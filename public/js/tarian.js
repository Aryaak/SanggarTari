const optionsImg = Array.from(document.getElementsByClassName('option-img'));
const tarianNama = Array.from(document.getElementsByClassName('tarian-nama'));
const video = document.getElementById('tarian-video');
const source = document.createElement('source');
let articles = document.getElementById('articles');

const setArticles = (index) => {
    articles.innerHTML = '';
    tarian[index].news.forEach(e => {
        articles.innerHTML += `<div data-aos="zoom-out" data-aos-duration="1000" class="text-white">
        <img class="border-2 border-white w-full md:w-72 md:h-48 h-72 mb-5" src="${e.img}" alt="Arikel">
        <h3 class="tarian-nama   font-bold  mb-3 text-base">${e.title}</h3>
        <p class="text-gray-400 text-sm">${e.text}</p >
            <div class="hover:text-white text-sm cursor-pointer text-primary flex items-center gap-x-1 mt-5">
                <p class="font-bold ">Baca selengkapnya</p>
                <i class="fas fa-arrow-circle-right mt-1"></i>
            </div>
        </div > `
    })
}
setArticles(0);


source.setAttribute('src', tarian[0].video);
video.appendChild(source);
video.play();
const changeTarian = (index) => {
    setArticles(index);

    optionsImg.forEach(e => {
        e.classList.remove('border-primary');
    })
    optionsImg[index].classList.add('border-primary');
    document.getElementsByClassName('tarian-box')[0].style.backgroundImage = "url('" + tarian[index].img + "')"
    document.getElementById('tarian-from').innerText = tarian[index].from;
    tarianNama.forEach(e => {
        e.innerText = tarian[index].name;
    })
    document.getElementById('tarian-history').innerText = tarian[index].history.text;
    document.getElementById('tarian-img').setAttribute('src', tarian[index].history.img);

    setTimeout(function () {
        video.pause();

        source.setAttribute('src', tarian[index].video);

        video.load();
        video.play();
    }, 1000);
}